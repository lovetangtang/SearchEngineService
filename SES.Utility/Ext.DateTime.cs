        public static string ToQuartString(this DateTime? dateTime) {
            if (dateTime == null)
                return string.Empty;
            return ToQuartString((DateTime)dateTime);
        }


        /// <summary>
        /// 日期格式转入季度字符串
        /// </summary>
        /// <param name="dateTime"></param>
        /// <returns></returns>
        public static string ToQuartString(this DateTime dateTime)
        {
            //yyyy年MM季度
            DateTime date = (DateTime)dateTime;
            int year = date.Year;
            int month = date.Month;
            string quartStr = "04季度";
            if (month < 4)
            {
                quartStr = "01季度";
            }
            else if (month < 7)
            {
                quartStr = "02季度";
            }
            else if (month < 10)
            {
                quartStr = "03季度";
            }
            return year + "年" + quartStr;
        }

        /// <summary>
        /// 
        /// </summary>
        /// <param name="quartString">yyyy年MM季度</param>
        /// <returns></returns>
        public static DateTime? ToDateFromQuartString(this string quartString) {
            Regex regex = new Regex(@"^(\d{4})年(\d{2})季度$");
            Match match=regex.Match(quartString);
            if (match.Groups.Count < 3) {
                return null;
            }
            int quartNum = match.Groups[2].Value.ToInt();
            string date = match.Groups[1].Value + "-" + (quartNum*3-2) + "-01";
            return date.ToDate();
        }
